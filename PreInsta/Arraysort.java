import java.util.Arrays;
public class Arraysort {
    public static void main(String[] args) {
        int[] arr = { 5, 2, 9, 8, 6, 1 };
        Arrays.sort(arr);
        System.out.println("Ascending: " + Arrays.toString(arr));
    }
}
